import UIKit

public extension UIColor
{
    class var primary: UIColor { return UIColor.black }

    class var secondary: UIColor { return UIColor.darkGray }
    
    class var background: UIColor { return UIColor.black }
    class var backgroundLight: UIColor { return UIColor.white }
    
    class var error: UIColor { return UIColor.red }
    
    class var divider: UIColor { return UIColor.lightGray }
    
    class var textWhite: UIColor { return UIColor.white }
    class var textDark: UIColor { return UIColor.black }
    class var brand: UIColor { return UIColor.white }
    
    class var input: UIColor { return UIColor.gray }
}
